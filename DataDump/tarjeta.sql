SELECT
    pMedia.CARD_NUMBER CardNumber,
    customer.DOCUMENT Document,
    gralAcc.INTERNAL_ACCOUNT_NUMBER InternalAccountNumber,
    pMediaStatus.DESCRIPTION StatusCode,
    (SELECT MIN(CHANGE_BUSINESS_DATE) FROM  ICS_PAYMENT_MEDIA_STATUS_H WHERE ID_PAYMENT_MEDIA_STATUS_OLD IS NULL AND ID_PAYMENT_MEDIA = pMedia.ID_PAYMENT_MEDIA) IssueDate,
    (SELECT MAX(CHANGE_BUSINESS_DATE) FROM  ICS_PAYMENT_MEDIA_STATUS_H WHERE ID_PAYMENT_MEDIA_STATUS_NEW in ( SELECT ID_PAYMENT_MEDIA_STATUS FROM IBC_PAYMENT_MEDIA_STATUS where STATUS_CODE in ('0012','0013') AND ID_ISSUER = :IdIssuer) AND ID_PAYMENT_MEDIA = pMedia.ID_PAYMENT_MEDIA) InactiveDate,
    pMedia.DUE_DATE DueDate,
    pMedia.PAYMENT_MEDIA_SEQUENCE_NUMB SecuenceNumber,
    customer.ID_DOCUMENT_TYPE DocumentType,
    docType.DESCRIPTION DocTypeDescription,
    finRelMain.FINANCIAL_RELATION_TYPE Category,
    issuerProduct.EXTERNAL_ISSUER_ID ProductCode,
    prod.PRODUCT_NAME ProductDescription,
    productGroup.PRODUCT_GROUP_CODE ProductGroupCode,
    lotItem.LOT_ITEM_TRACKING_NUMBER TrackingID,
    pMediaStatus.STATUS_TYPE PaymentMediaStatusType,
    customer.EMBOSSING_NAME EmbossingName,
    gralAcc.SCHEME_TYPE Scheme,
    mSupportType.DESCRIPTION CardTechnology,
    -- falta SDP
    accStatus.STATUS_CODE       BlockStatusCode,
    blockCat.CATEGORY_CODE                BlockCategoryCode,
    tokenTable.BIN BIN,
    -- ICA -- falta ICA
    -- ChipSequence -- falta ChipSequence
    FORMAT(attReqOrigin.ATTENDANCE_REAL_DATE, 'HHmmss') EmissionTime,
    FORMAT(attReqOrigin.NEW_DUE_DATE, 'yyyyMMdd') PreviousDueDate,
    CASE attReqDest.ATTENDANCE_REQUEST_TYPE
        WHEN 1 THEN 'CARD_REEMISSION'
        WHEN 2 THEN 'CARD_RENEWAL'
        WHEN 3 THEN 'PIN_REEMISSION'
        ELSE NULL
    END ActionToken
FROM
    IAC_PAYMENT_MEDIA pMedia
        INNER JOIN ICS_PAYMENT_MEDIA_RELATION pmRel ON pmRel.ID_PAYMENT_MEDIA = pMedia.ID_PAYMENT_MEDIA
        INNER JOIN ICS_ACCOUNT_RELATION accRel ON accRel.ID_ACCOUNT_RELATION = pmRel.ID_ACCOUNT_RELATION
        INNER JOIN ICS_FINANCIAL_RELATION finRelMain ON finRelMain.ID_FINANCIAL_RELATION = accRel.ID_FINANCIAL_RELATION
        INNER JOIN ICS_CUSTOMER customer ON finRelMain.ID_CUSTOMER = customer.ID_CUSTOMER
        INNER JOIN IAC_GENERAL_ACCOUNT gralAcc ON gralAcc.ID_GENERAL_ACCOUNT = finRelMain.ID_GENERAL_ACCOUNT
        INNER JOIN IBC_PAYMENT_MEDIA_STATUS pMediaStatus on pMediaStatus.ID_PAYMENT_MEDIA_STATUS = pMedia.ID_PAYMENT_MEDIA_STATUS
        INNER JOIN TGD_DOCUMENT_TYPE docType ON docType.ID_DOCUMENT_TYPE = customer.ID_DOCUMENT_TYPE
        INNER JOIN IBC_PRODUCT prod ON prod.ID_PRODUCT = pMedia.ID_PRODUCT
        INNER JOIN IBC_ISSUER_PRODUCT issuerProduct ON issuerProduct.ID_PRODUCT = prod.ID_PRODUCT
        LEFT JOIN ILG_LOT_ITEM_CARD lotItemCard ON lotItemCard.ID_PAYMENT_MEDIA = pMedia.ID_PAYMENT_MEDIA
        LEFT JOIN ILG_LOT_ITEM lotItem ON lotItem.ID_LOT_ITEM = lotItemCard.ID_LOT_ITEM
        INNER JOIN IBC_PRODUCT_GROUP productGroup ON productGroup.ID_PRODUCT_GROUP = gralAcc.ID_PRODUCT_GROUP
        INNER JOIN IAC_MEDIA_SUPPORT mSupport ON mSupport.ID_MEDIA_SUPPORT = pMedia.ID_MEDIA_SUPPORT
        LEFT JOIN IBC_MEDIA_SUPPORT_TYPE mSupportType ON mSupportType.ID_MEDIA_SUPPORT_TYPE = mSupport.MEDIA_SUPPORT_TYPE
        INNER JOIN IBC_ACCOUNT_STATUS accStatus
                   ON accStatus.ID_ACCOUNT_STATUS = gralAcc.ID_ACCOUNT_STATUS
        LEFT JOIN IBC_ACCOUNT_STATUS_BLOCK_REASONS blockReason
                  ON blockReason.ID_ACCOUNT_STATUS = accStatus.ID_ACCOUNT_STATUS
        LEFT JOIN IBC_ACCOUNT_STATUS_BLOCK_REASON_CATEGORIES blockCat
                  ON blockCat.ID_ACCOUNT_STATUS_BLOCK_REASON = blockReason.ID_ACCOUNT_STATUS_BLOCK_REASON
        INNER JOIN IBC_ISSUER_PRODUCT_TOKEN_TABLE tokenTable
                  ON tokenTable.ID_ISSUER = :IdIssuer
        LEFT JOIN ICS_ATTENDANCE_REQUEST attReqOrigin
                  ON attReqOrigin.ID_PAYMENT_MEDIA_ORIGIN = pMedia.ID_PAYMENT_MEDIA
        LEFT JOIN ICS_ATTENDANCE_REQUEST attReqDest
                  ON attReqDest.ID_PAYMENT_MEDIA_DEST = pMedia.ID_PAYMENT_MEDIA
WHERE
    pMedia.ID_ISSUER = :IdIssuer
  AND customer.IS_INNOMINATE =0

;

