SELECT
    O.ID_OPERATION AS IdOperation,
    ACC.INTERNAL_ACCOUNT_NUMBER AS Account,
    FORMAT(O.BUSINESS_DATE,'yyyyMMdd') AS OperationDate,
    O.ID_CURRENCY AS Currency,
    O.OPERATION_SIGN AS Sign,
    O.AMOUNT AS OperationAmount,
    SUM(CASE WHEN M.MOVEMENT_TYPE <> 6 AND I.EXTERNAL_NUMBER IN ('0017','0019','0021','0022','0024','0032','0036','0050') THEN ISNULL(M.AMOUNT,0) ELSE 0 END) AS AffectedInterestTaxed,
    SUM(CASE WHEN M.MOVEMENT_TYPE <> 6 AND I.EXTERNAL_NUMBER IN ('0018','0020','0023','0033','0037') AND M.MOVEMENT_SIGN = 1 THEN ISNULL(M.AMOUNT,0) ELSE 0 END) AS AffectedInterestExcent,
    SUM(CASE WHEN M.MOVEMENT_TYPE = 6 THEN M.AMOUNT ELSE 0 END) AS Tax,
    CASE
        WHEN O.OPERATION_ENTRY_CHANNEL = 1 THEN 0        -- Local input (online)
        WHEN O.OPERATION_ENTRY_CHANNEL IN (2, 4) THEN 1  -- System-generated
        WHEN O.OPERATION_ENTRY_CHANNEL = 3 THEN 2        -- User input
        END as SourceCode,
    M.TRANS_CODE as TransCode
FROM IOP_OPERATION O
         INNER JOIN IFN_MOVEMENT M ON O.ID_OPERATION = M.ID_OPERATION
         INNER JOIN IFN_ITEM I ON I.ID_ITEM = M.ID_ITEM
         INNER JOIN IAC_GENERAL_ACCOUNT ACC ON ACC.ID_GENERAL_ACCOUNT =O.ID_GENERAL_ACCOUNT
         INNER JOIN IFN_ITEM_CONFIG IC ON IC.ID_ITEM =I.ID_ITEM AND  ACC.ID_ISSUER =IC.ID_ISSUER AND IC.ITEM_CONFIG_CODE =:itemConfigCode
         INNER JOIN IBC_BUSINESS_DATE BD ON BD.ID_ISSUER=IC.ID_ISSUER
WHERE
    O.OPERATION_TYPE IN (106,108)
  AND M.BUSINESS_DATE= DATEADD (DD,-1,BD.TODAY_DATE)
  AND ACC.ID_ISSUER =:idIssuer
GROUP BY
    O.ID_OPERATION,ACC.INTERNAL_ACCOUNT_NUMBER, O.BUSINESS_DATE, O.ID_CURRENCY, O.OPERATION_SIGN, O.AMOUNT, M.TRANS_CODE, O.OPERATION_ENTRY_CHANNEL