SELECT f.ID_FILE_TYPE, t.TYPE_NAME, f.ID_ISSUER, f.FIELD, f.IS_VISIBLE, f.LENGTH
FROM IBC_ISSUER_FILE_TYPE_FIELDS f
JOIN TFM_FILE_TYPE t ON t.ID_FILE_TYPE = f.ID_FILE_TYPE
WHERE f.ID_ISSUER = 999
  AND (
       (f.FIELD = 'PostalCode' AND t.TYPE_NAME IN ('DATA_DUMP_RPT_ACCOUNT_CLIENT', 'DATA_DUMP_RPT_EMPLOY', 'DATA_DUMP_RPT_CLIENT'))
    OR (f.FIELD = 'Zipcode'   AND t.TYPE_NAME = 'DATA_DUMP_RPT_CONFIRMED_TRANSACTIONS')
    OR (f.FIELD = 'ZipCode'   AND t.TYPE_NAME = 'DATA_DUMP_RPT_TRANSACTIONS')
  )
ORDER BY t.TYPE_NAME, f.FIELD;