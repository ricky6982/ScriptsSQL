SELECT
    OP.ID_OPERATION IdOperation,
    IGA.INTERNAL_ACCOUNT_NUMBER InternalAccountNumber,
    OP.VALUE_DATE OperationDate,
    OP.OPERATION_TYPE OperationType,
    OP.ID_CURRENCY Currency,
    OP.AMOUNT OperationAmount,
    OP.OPERATION_SIGN OperationSign,
    OP.DESCRIPTION Description,
    OC.OPERATION_CATEGORY_CODE CategoryCode,
    OC.OPERATION_CATEGORY_NAME CategoryName,
    OP.INSTALLMENT_QUANTITY InstallmentQuantity,
    ibcplan.PLAN_CODE SalePlanCode,
    ibcplan.PLAN_NAME SalePlanName,
    OP.INTEREST_RATE InterestRate,
    ibcper.INSTALLMENT_PERIODICITY Periodicity,
    mov.TRANS_CODE TransactionCode,
    CASE OP.OPERATION_ENTRY_CHANNEL
        WHEN 1 THEN 0 -- Local input (online)
        WHEN 2 THEN 1 -- System-generated
        WHEN 4 THEN 1 -- System-generated
        WHEN 3 THEN 2 -- User input
        ELSE 2 -- User input
        END as SourceCode
FROM  IOP_OPERATION OP
          INNER JOIN IAC_GENERAL_ACCOUNT IGA ON OP.ID_GENERAL_ACCOUNT = IGA.ID_GENERAL_ACCOUNT
          LEFT JOIN IFN_MANUAL_OPERATION MO ON MO.ID_MANUAL_OPERATION = OP.ID_OPERATION
          LEFT JOIN IFN_OPERATION_CATEGORY OC ON  MO.ID_OPERATION_CATEGORY = OC.ID_OPERATION_CATEGORY
          LEFT JOIN IBC_FINAN_MAN_OP_REFINANCE_PLAN ibcplan ON ibcplan.ID_FINAN_OP_REFINANCE_PLAN = op.ID_FINAN_MAN_OP_REFINANCE_PLAN
          LEFT JOIN IBC_FINAN_MAN_OP_PERIODICITY ibcper ON ibcper.ID_FINAN_OP_PERIODICITY = ibcplan.ID_FINAN_OP_PERIODICITY
          LEFT JOIN IFN_MOVEMENT mov ON mov.ID_OPERATION = OP.ID_OPERATION
WHERE IGA.ID_ISSUER = :IdIssuer AND (MO.ID_MANUAL_OPERATION IS NOT NULL OR OP.OPERATION_TYPE=:ManualOperationType OR OP.OPERATION_TYPE=:RefinancingOperationType OR OP.ID_FINAN_MAN_OP_REFINANCE_PLAN IS NOT NULL)
  AND CAST(OP.BUSINESS_DATE AS DATE) = :FilterDate