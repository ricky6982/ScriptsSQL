select ft.TYPE_NAME, count(*)
from
    [IBC_ISSUER_FILE_TYPE_FIELDS] isf
        inner join TFM_FILE_TYPE ft on isf.ID_FILE_TYPE = ft.ID_FILE_TYPE
WHERE ID_ISSUER = 999
group by isf.ID_FILE_TYPE, ft.TYPE_NAME
