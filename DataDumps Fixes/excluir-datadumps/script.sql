SELECT
    ft.*,
    '-' as separator,
    IFT.ID_ISSUER_FILE_TYPE IdIssuerFileType,
    IFT.ID_ISSUER IdIssuer,
    IFT.ID_FILE_TYPE IdFileType,
    FT.TYPE_NAME,
    IFT.ID_PROCESS IdProcess,
    IFT.FORMAT Format,
    IFT.EXTENSION Extension,
    IFT.ENCODING Encoding,
    IFT.STX_REQUIRED IsStxRequired
FROM TBE_PROCESS P
         INNER JOIN TBE_BATCH_PROCESS BP ON P.ID_PROCESS = BP.ID_PROCESS
         INNER JOIN TFM_FILE_TYPE FT ON FT.ID_PROCESS = BP.ID_PROCESS
         INNER JOIN IBC_ISSUER_FILE_TYPE IFT ON IFT.ID_FILE_TYPE = FT.ID_FILE_TYPE
WHERE P.OPERATION_NAME = 'DataDumpRpt'
  AND IFT.ID_ISSUER = :IdIssuer;

