    TOT.TRANSACTION_PAN TransactionPan,
    PROD.ID_PRODUCT IdProduct,
    PROD.PRODUCT_NAME ProductName,
    PROD.ID_BRAND IdBrand,
    BRAND.NAME BrandName,
    TOT.MCC_CODE IdMcc,
    MCC.DESCRIPTION MccDescription,
    TOT.ACQUIRING_INSTITUTION_CODE AcquiringInstitutionCode,
    TOT.POS_CONDITION_CODE POSConditionCode,
    TID.ID_SALE_PLAN IdSalePlan,
    TOT.MCC_CODE MccCode,
    TOT.EXPIRATION_DATE ExpirationDate,
    TOT.PAN_SEQUENCE_NUMBER PaymentMedSequenceNumber,
    TRXSTA.DESCRIPTION TransactionStatusDescription,
    TRXSTA.ID_TRANSACTION_STATUS IdTransactionStatus,
    TOT.MTI_CODE MTI,
    TOT.PROCESSING_CODE ProcessingCode,
