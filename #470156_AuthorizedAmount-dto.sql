    currencyAuthorized.DISPLAY_SYMBOL AuthorizedCurrencySymbol,
    currencyAuthorized.DECIMAL_DIGITS AuthorizedDecimalDigits,

    CASE
        WHEN TID.IS_PARTIAL_AUTHORIZATION = 1 THEN 'YES'
        WHEN TID.IS_PARTIAL_AUTHORIZATION = 0 THEN 'NO'
        WHEN TID.IS_PARTIAL_AUTHORIZATION IS NULL AND TOT.CARD_BILLING_AMNT > TOT.CHARGED_AMOUNT AND TOT.CHARGED_AMOUNT > 0  THEN 'YES'
        ELSE 'NO'
        END AuthorizedApplyPartial,
    CASE
        WHEN auiOpenToken.ID_TRANSACTION IS NULL THEN 'NO'
        ELSE 'YES'
        END IsTokenized,
    COALESCE(PROD.PRODUCT_TYPE, (SELECT PRODUCT_TYPE FROM AUI_PRODUCT WHERE ID_PRODUCT = TID.ID_PRODUCT), 1) AS PRODUCT_TYPE
FROM
    AU_OPEN_TRANSACTION TOT
        INNER JOIN T_CURRENCY CURR ON CURR.DEFAULT_NUMERIC_CODE = TOT.MESSAGE_CURRENCY_CODE
        INNER JOIN AU_TRANSACTION_STATUS TRXSTA ON TRXSTA.ID_TRANSACTION_STATUS = TOT.ID_TRANSACTION_STATUS
        INNER JOIN AUI_TRX_ISSUER_DATA TID ON TID.ID_TRANSACTION = TOT.ID_TRANSACTION
        LEFT OUTER JOIN T_CURRENCY CBCURR ON CBCURR.DEFAULT_NUMERIC_CODE = TOT.CARD_BILLING_CURRENCY_CODE
        LEFT OUTER JOIN AUI_PAYMENT_MEDIA PM ON PM.ID_PAYMENT_MEDIA =  TID.ID_PAYMENT_MEDIA
        LEFT OUTER JOIN AUI_PAYMENT_MEDIA_RELATION PMR ON PMR.ID_PAYMENT_MEDIA = PM.ID_PAYMENT_MEDIA
        LEFT OUTER JOIN AUI_ACCOUNT_RELATION AR ON PMR.ID_ACCOUNT_RELATION = AR.ID_ACCOUNT_RELATION
        LEFT OUTER JOIN AUI_FINANCIAL_RELATION FR ON AR.ID_FINANCIAL_RELATION = FR.ID_FINANCIAL_RELATION
        LEFT OUTER JOIN AUI_GENERAL_ACCOUNT GA ON GA.ID_GENERAL_ACCOUNT = FR.ID_GENERAL_ACCOUNT
        LEFT OUTER JOIN AU_MCC MCC ON MCC.ID_MCC = TOT.MCC_CODE
        LEFT OUTER JOIN AU_RESPONSE_CODE RESCOD ON RESCOD.ID_RESPONSE_CODE = TOT.ID_RESPONSE_CODE
        LEFT OUTER JOIN AUI_PRODUCT PROD ON PROD.ID_PRODUCT = PM.ID_PRODUCT
        LEFT OUTER JOIN AU_BRAND BRAND ON BRAND.ID_BRAND = PROD.ID_BRAND
        LEFT OUTER JOIN T_COUNTRY TCOUNTRY ON TCOUNTRY.NUMERIC_CODE = TOT.ACQUIRER_COUNTRY_CODE
        LEFT OUTER JOIN T_COUNTRY TCOUNTRYTRANSACTION ON (TCOUNTRYTRANSACTION.ISO_CODE = TOT.TRANSACTION_COUNTRY_CODE OR TCOUNTRYTRANSACTION.CHAR_CODE = TOT.TRANSACTION_COUNTRY_CODE)
        LEFT OUTER JOIN AUI_SALE_PLAN SPLAN ON SPLAN.ID_SALE_PLAN = TID.ID_SALE_PLAN
        LEFT OUTER JOIN AUI_CUSTOMER CUSTOMER ON CUSTOMER.ID_CUSTOMER = FR.ID_CUSTOMER
        LEFT OUTER JOIN AUI_ACCOUNT_STATUS accStatus ON accStatus.ID_ACCOUNT_STATUS = GA.ID_ACCOUNT_STATUS
        LEFT OUTER JOIN AUI_ISSUER_PRODUCT ip ON ip.ID_ISSUER = TID.ID_ISSUER AND ip.ID_PRODUCT = PROD.ID_PRODUCT
        LEFT OUTER JOIN T_CURRENCY currencyAuthorized ON TOT.CARD_BILLING_CURRENCY_CODE = currencyAuthorized.ID_CURRENCY
        LEFT JOIN AUI_OPEN_TOKENIZATION auiOpenToken ON auiOpenToken.ID_TRANSACTION = TOT.ID_TRANSACTION
--where     TOT.CHARGED_AMOUNT > 0

WHERE

   (TID.ID_ISSUER = 100)
and      TOT.CHARGED_AMOUNT > 0
